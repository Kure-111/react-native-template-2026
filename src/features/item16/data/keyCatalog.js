const normalizeText = (value) => (value || '').trim();

const parseCsvLine = (line) => {
  if (!line) {
    return [];
  }

  const columns = [];
  let current = '';
  let inQuotes = false;

  for (let i = 0; i < line.length; i += 1) {
    const char = line[i];
    if (char === '"') {
      inQuotes = !inQuotes;
      continue;
    }

    if (char === ',' && !inQuotes) {
      columns.push(current);
      current = '';
      continue;
    }

    current += char;
  }

  columns.push(current);
  return columns;
};

const parseKeyCatalog = (csvText) => {
  const lines = csvText
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);

  if (lines.length <= 1) {
    return [];
  }

  return lines
    .slice(1)
    .map((line) => {
      const columns = parseCsvLine(line);
      const [idRaw, nameRaw, buildingRaw, locationRaw] = columns;

      const id = normalizeText(idRaw);
      const name = normalizeText(nameRaw);
      const building = normalizeText(buildingRaw);
      const location = normalizeText(locationRaw);

      if (!id || !name || !building) {
        return null;
      }

      return {
        id,
        keyCode: id,
        name,
        building,
        location: location || `${building} ${name}`,
      };
    })
    .filter(Boolean);
};

